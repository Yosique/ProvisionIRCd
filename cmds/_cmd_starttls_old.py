def cmd_STARTTLS(self, localServer, recv):
    try:
        if type(self).__name__ != 'Server':
            self.sendraw(487,':STARTTLS is a server only command')
            return
        self._send(':{} 670 {} :STARTTLS successful, proceed with TLS handshake'.format(localServer.hostname,self.hostname))
        #self._send('STARTTLS')
        return
        if self.socket:
            certFile = '{}/ssl/server.cert.pem'.format(localServer.rootdir)
            keyFile = '{}/ssl/server.key.pem'.format(localServer.rootdir)
            self.socket = ssl.SSLSocket(self.socket,
                    server_side=True,
                    do_handshake_on_connect=False,
                    certfile=certFile, keyfile=keyFile,
                    #cert_reqs=ssl.CERT_OPTIONAL,
                    suppress_ragged_eofs=True,
                    ciphers=ciphers
                    )
    except Exception as ex:
        exc_type, exc_obj, exc_tb = sys.exc_info()
        fname = os.path.split(exc_tb.tb_frame.f_code.co_filename)[1]
        e = 'EXCEPTION: {} in file {} line {}: {}'.format(exc_type.__name__,fname,exc_tb.tb_lineno,exc_obj)
        print(e)
